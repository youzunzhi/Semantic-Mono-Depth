if not os.path.exists(args.output_dir):
    os.mkdir(args.output_dir)

sems= np.load(args.pred_path)
with open(args.filelist) as filelist:
    for idx,line in enumerate(filelist):
        sem=sems[idx]
        fpath = os.path.join(args.output_dir,line.split(" ")[-1].strip().split("/")[-1])
        print(fpath)
        cv2.imwrite(fpath , sem.astype(np.uint8))

        fpath_color = os.path.join(args.output_dir,line.split(" ")[-1].strip().split("/")[-1].replace(".","_color."))
        sem = np.stack([sem,sem,sem],axis=-1)
        sem_color = cv2.LUT(sem.astype(np.uint8),lutColor)
        cv2.imwrite(fpath_color,sem_color)
        


